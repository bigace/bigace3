class Admin_View_Helper_ApproveLink extends Admin_View_Helper_ActionLink
{
    public function approveLink($href = null, $attribs = array())
    {
        $text = getTranslation('approve');
        if(isset($attribs['text']))
            $text = $attribs['text'];
        return parent::actionLink($text, 'approve', $href, $attribs);
    }
}
